// 자바빈, DTO(Data Transfer Object)

package javaBean;

public class SimpleBean {

	private String msg;   // 프로퍼티(Property),필드,맴버변수
	
	private String name;

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
